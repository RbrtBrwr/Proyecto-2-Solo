    /**
     * elimina el archivo indicado de la lista de archivos del usuario
     * @param evt 
     */
    private void destroyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_destroyActionPerformed
        boolean eliminado = false;
        String archivo = fileNameElim.getText();
        String usuario = userNameElim.getText();
        if (archivo.length() < 3){
            anuncio.setText("Nombre de Archivo invalido");
            return;
        } 
        if (usuario.length() < 3){
            anuncio.setText("Nombre de usuario invalido");
        }
        
        User user = Inicio.dispersionUsuarios.getUser(usuario);
        if (user != null){
            eliminado = user.documentos.eliminarArchivo(archivo);
        }
        
        if (eliminado){
            anuncio.setText("Archivo eliminado exitosamente");
        }
        
        
    }//GEN-LAST:event_destroyActionPerformed

