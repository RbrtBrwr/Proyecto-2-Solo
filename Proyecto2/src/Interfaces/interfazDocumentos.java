    /**
     * Crea un documento nuevo y lo agrega a ala lista de documentos del usuario
     * @param evt 
     */
    private void createActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createActionPerformed
        User usuario = Inicio.dispersionUsuarios.getUser(userName.getText());
        if (usuario != null){
            String nombreArchivo = fileName.getText();
            if (nombreArchivo.length() > 3){
                
                int tamano = Integer.parseInt(fileSize.getText());
                String type = usuario.getPriority();
                int priority;
                if (tamano > 0){
                    switch (type){
                        case "prioridad_alta":
                            priority = 0;
                            break;
                        case "prioridad_media":
                            priority = 1;
                            break;
                        default:
                            priority = 2;
                            break;
                            
                    }
                    
                    Document nuevo = new Document(nombreArchivo, tamano, priority);
                    nuevo.setType(typeDoc.getSelectedItem().toString());
                    usuario.agregarDocs(nuevo);
                    anuncio.setText("El Usuario " + userName.getText() + " ha creado el siguiente documento: " 
                                        + nombreArchivo + nuevo.getType() + ":\nTamano: " + tamano);
                    return;

                }
                anuncio.setText("Tamano invalido");
                return;
            }
            anuncio.setText("Nombre invalido");
            return;        
        }
        anuncio.setText("Usuario " + userName.getText() + " no existe, por favor crear usuario");
    }//GEN-LAST:event_createActionPerformed

