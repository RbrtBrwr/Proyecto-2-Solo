    /**
     * carga un csv con los usuarios y sus prioridades
     * @param evt 
     */
    private void cargaUsuariosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cargaUsuariosActionPerformed
        String usuariosCargados = csvOpen.read();
        if (usuariosCargados == null || usuariosCargados.isBlank()){
            anuncio.setText("Archivo invalido");
            return;
        }
        String[] informacion = usuariosCargados.split("\n");
        String[][] usuarios = new String[informacion.length][2];
        for (int i = 0; i < informacion.length; i++){
            usuarios[i] = informacion[i].split(",");
        }
        String anuncioUsuarios = "Usuarios Cargados:\n";
        for (int j = 1; j < informacion.length; j++){
            User user = new User(usuarios[j][0], usuarios[j][1]);
            anuncioUsuarios += usuarios[j][0] + ": " + usuarios[j][1] + "\n";
            Inicio.dispersionUsuarios.agregar(user);
        }
        anuncio.setText(anuncioUsuarios);

    }//GEN-LAST:event_cargaUsuariosActionPerformed

