    /**
     * Agrega un usuario al sistema
     * @param evt 
     */
    private void buttonAddUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonAddUserActionPerformed
        String nombre = addUser.getText();
        String prioridad = choosePriority.getSelectedItem().toString();
        if (nombre.length() < 2){
            anuncio.setText("Nombre invalido");
        } else {
            switch (prioridad){
                case "Prioridad Alta":
                    prioridad = "prioridad_alta";
                    break;
                case "Prioridad Media":
                    prioridad = "prioridad_media";
                    break;
                case "Prioridad Baja":
                    prioridad = "prioridad_baja";
                    break;
                default:
                    anuncio.setText("Prioridad invalida");
                    return; 
            }
                    
            User nuevo = new User(nombre, prioridad);
            
            addUser.setText("");
            anuncio.setText(Inicio.dispersionUsuarios.agregar(nuevo));
        }
    }//GEN-LAST:event_buttonAddUserActionPerformed

    /**
     * Guarda los usuarios del sistema en un archivo csv
     * @param evt 
     */
    private void guardarUsuariosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarUsuariosActionPerformed
        String usuariosGuardados = Inicio.dispersionUsuarios.paraGuardar();
        if (csvOpen.write(usuariosGuardados)){
            anuncio.setText("Se han guardado los usuarios de la siguiente manera:\n" + usuariosGuardados);
        } else {
            anuncio.setText("Archivo invalido");
        }
        
    }//GEN-LAST:event_guardarUsuariosActionPerformed

    /**
     * elimina un usuario del sistema
     * @param evt 
     */
    private void buttonElimUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonElimUserActionPerformed
        String nombreEliminado = elimUser.getText();
        if (nombreEliminado.length() > 3){
            anuncio.setText(Inicio.dispersionUsuarios.eliminarUsuario(elimUser.getText()));
        } else {
            anuncio.setText("Nombre invalido");
        }
    }//GEN-LAST:event_buttonElimUserActionPerformed

